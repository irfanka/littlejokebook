import os
import json
import django
from pydantic import BaseModel

os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'little_jokebook.settings')
django.setup()

from catalogue.models import Segment, Video
from google import genai
from google.genai import types

class SegmentEval(BaseModel):
    segment_id: int
    is_correct: bool
    reason: str

class BatchEvalResult(BaseModel):
    evaluations: list[SegmentEval]

EVAL_PROMPT = """\
You are an expert evaluator of video segmentation pipelines.

Below is a chronological list of segments generated by a system for a comedy show, \
along with a snippet of the ACTUAL transcript (first and last few lines) for those exact time windows.

The system has a known failure mode where it correctly identifies the first few segments, \
but then stops watching the video and starts hallucinating a rigid timeline, completely decoupling \
from reality (e.g., claiming Comedian X is performing, when the transcript clearly shows \
the host is still interviewing Comedian Y, or doing an ad read).

Your task is to evaluate the ENTIRE sequence and grade EACH segment as correct or incorrect.
- A segment is CORRECT if the generated Description/Summary accurately reflects the people and events in the transcript.
- A segment is INCORRECT if it claims someone is performing or being interviewed, but the transcript clearly shows someone else, or a different event entirely.

Analyze the flow carefully to pinpoint exactly where the model decoupled from reality.
"""

def build_prompt(segments):
    prompt = EVAL_PROMPT + "\n\n--- TIMELINE ---\n\n"
    
    for s in segments:
        prompt += f"Segment ID: {s.id} | Window: [{s.start_time}s - {s.end_time}s]\n"
        prompt += f"Generated Type: {s.segment_type}\n"
        prompt += f"Generated Description: {s.description}\n"
        prompt += f"Generated Summary: {s.summary}\n"
        
        transcript_data = s.transcript
        if isinstance(transcript_data, str):
            try:
                transcript_data = json.loads(transcript_data)
            except:
                transcript_data = []
                
        prompt += "Actual Transcript:\n"
        if not transcript_data:
            prompt += "  (No transcript available)\n"
        else:
            for idx, line in enumerate(transcript_data):
                if idx < 3 or idx >= len(transcript_data) - 3:
                    prompt += f"  {line.get('timestamp', '?')}s | {line.get('speaker', '?')}: {line.get('text', '?')}\n"
                elif idx == 3:
                    prompt += "  ... (skipped)\n"
        prompt += "\n"
        
    return prompt

def main():
    videos = Video.objects.all()
    if not videos:
        print("No videos found.")
        return

    v = videos.first()
    print(f"Evaluating Video: {v.url} (ID: {v.id})")
    
    segments = list(Segment.objects.filter(video=v).order_by('start_time'))
    if not segments:
        print("No segments found for this video.")
        return

    prompt = build_prompt(segments)
    
    client = genai.Client(api_key=os.environ["GEMINI_API_KEY"])
    
    print("\nSending chronological timeline to gemini-3.1-pro-preview...")
    
    response = client.models.generate_content(
        model="gemini-3.1-pro-preview",
        contents=prompt,
        config=types.GenerateContentConfig(
            response_mime_type="application/json",
            response_schema=BatchEvalResult,
            temperature=0.1,
        )
    )
    
    result = BatchEvalResult.model_validate_json(response.text)
    
    correct_count = 0
    
    # Create a quick lookup for segment times
    seg_lookup = {s.id: s for s in segments}
    
    print("\n" + "="*50)
    print("EVALUATION RESULTS:")
    print("="*50 + "\n")
    
    for ev in result.evaluations:
        s = seg_lookup.get(ev.segment_id)
        time_window = f"[{s.start_time}s - {s.end_time}s]" if s else "[Unknown]"
        status = "✅ CORRECT" if ev.is_correct else "❌ INCORRECT"
        
        if ev.is_correct:
            correct_count += 1
            
        print(f"Segment ID {ev.segment_id} {time_window}: {status}")
        print(f"Reason: {ev.reason}\n")
        
    print("="*50)
    print(f"FINAL SCORE: {correct_count}/{len(segments)} ({(correct_count/len(segments))*100:.1f}%)")

if __name__ == '__main__':
    main()
